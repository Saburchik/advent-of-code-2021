function calculateScore(line) {
    let stack = []
    for (let char of line) {
        switch (char) {
            case '(': {
                stack.push(')')
                break
            }
            case '[': {
                stack.push(']')
                break
            }
            case '{': {
                stack.push('}')
                break
            }
            case '<': {
                stack.push('>')
                break
            }
            case ')':
            case ']':
            case '}':
            case '>': {
                let expected = stack.pop()
                if (char !== expected) {
                    return 0
                }
            }
        }
    }
    if (stack.length === 0) {
        return 0
    }
    let lineScore = 0
    let char;
    while ((char = stack.pop())) {
        lineScore *= 5
        switch (char) {
            case ')': {
                lineScore += 1
                break
            }
            case ']': {
                lineScore += 2
                break
            }
            case '}': {
                lineScore += 3
                break
            }
            case '>': {
                lineScore += 4
                break
            }
        }
    }
    return lineScore
}

let lines = rawInput.split('\n')

let scores = []
for (let line of lines) {
    let lineScore = calculateScore(line)
    if (lineScore !== 0) {
        scores.push(lineScore)
    }
}

scores.sort((a, b) => a > b ? 1 : -1)

